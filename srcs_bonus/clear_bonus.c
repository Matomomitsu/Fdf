#include "../incs/fdf_bonus.h"

void	clear_img(t_fdf *fdf)
{
	size_t	x;
	size_t	y;

	x = 0;
	y = 0;
	while (y <= 900)
	{
		while (x <= 1200)
		{
			my_pixel_put(fdf, x, y, 0x00000000);
			x++;
		}
		x = 0;
		y++;
	}
}
